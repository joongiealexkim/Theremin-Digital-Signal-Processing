        // so when we multiply, we need to get a signed value out of it.

        MPY32CTL0 |= MPYM_1;

        MPYS32L = (int) (freq_base_log & 0xFFFF);
        MPYS32H = (int) (freq_base_log >> 16);
        OP2L = (int) (n24 & 0xFFFF);
        OP2H = (int) (n24 >> 16);
        __delay_cycles(11);
        //32 iq24 * 32bit iq24 = 64bit iq48
